    p_nombre             IN alumno.nombre%TYPE,
    p_apellido           IN alumno.apellido%TYPE,
    p_fecha_nacimiento   IN alumno.fecha_nacimiento%TYPE,
    p_nivel              IN alumno.nivel%TYPE,
    p_id_apoderado       IN alumno.id_apoderado%TYPE

)

AS

    v_existe_alumno      NUMBER;
    v_existe_apoderado   NUMBER;

BEGIN

    --------------------------------------------------
    -- Verificar que exista el alumno
    --------------------------------------------------

    SELECT COUNT(*)

    INTO v_existe_alumno

    FROM alumno

    WHERE id_alumno = p_id_alumno;

    IF v_existe_alumno = 0 THEN

        RAISE_APPLICATION_ERROR(
            -20061,
            'El alumno no existe.'
        );

    END IF;

    --------------------------------------------------
    -- Verificar que exista el apoderado
    --------------------------------------------------

    SELECT COUNT(*)

    INTO v_existe_apoderado

    FROM apoderado

    WHERE id_apoderado = p_id_apoderado;

    IF v_existe_apoderado = 0 THEN

        RAISE_APPLICATION_ERROR(
            -20062,
            'El apoderado no existe.'
        );

    END IF;

    --------------------------------------------------
    -- Validar nombre
    --------------------------------------------------

    IF TRIM(p_nombre) IS NULL THEN

        RAISE_APPLICATION_ERROR(
            -20063,
            'Debe ingresar el nombre del alumno.'
        );

    END IF;

    --------------------------------------------------
    -- Validar apellido
    --------------------------------------------------

    IF TRIM(p_apellido) IS NULL THEN

        RAISE_APPLICATION_ERROR(
            -20064,
